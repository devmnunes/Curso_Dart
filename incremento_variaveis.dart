void main() {

var numero;
numero = 10;

//numero++ : numero = numero + 1;
//numero-- : numero = numero - 1;
//numero+=2 : numero = numero +2; adiciona a quantidade desejada, adicionei apenas 2.
//numero-=2 : numero = numero -2; subtrai a quantidade desejada, subtrai apenas 2.


print(numero); //Printa valor inicial da variavel 10;
print(numero++); //Printa valor inicial da variavel 10 e adiciona +1, valor da variavel agora é 11;
print(++numero); //Adiciona +1 ao valor da variavel e Printa, Valor da variavel agora é 12;
print(--numero); //Subtrai -1 ao valor da variavel e Printa, valor da variavel agora é 11;
print(numero--); //Printa o valor da variavel 11 e subtrai -1, valor da variavel volta a ser 10.
print(numero);
}